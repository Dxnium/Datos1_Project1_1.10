package GUI;

public class letterOnMatrix {
	int posX;
	int posY;
	public letterOnMatrix(int posX,int posY) {
		this.setPosX(posX);
		this.setPosX(posY);
	}
	
	public int getPosX() {
		return posX;
	}

	public void setPosX(int posX) {
		this.posX = posX;
	}

	public int getPosY() {
		return posY;
	}

	public void setPosY(int posY) {
		this.posY = posY;
	}

	
}
